package lessons;

public class ForLoop {
    public static void main(String[] args) {
        //formula=> for(..sentence 3 ){sout}

        for (int i =3; i <10; i++){
            System.out.print(i+ "\t");
        }
        /**
         * int i=3; =>initialize statement
         * i<10; => condition statement
         * i++ => count ++ --
         */



    }
}
